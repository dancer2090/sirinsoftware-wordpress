						For you, these specialists are not on staff — they are “out of the staff.” 
						Outstaffing companies are responsible for their specialists’ official employment, 
						taxes, insurance and all that stuff. But you manage their work, 
						guide their activities and control the whole process. 
					</p>
				</div>
				<p>
					Basically, outstaffing entails the transfer of some employees working in one company to another legal entity who, 
					in turn, incurs full legal responsibility for them; The “transferred” employees continue to work at their regular 
					positions at the “host company,” but the responsibilities for maintaining their documentation, related salaries 
					and taxes, and other work issues become the responsibility of the “provider company” to whom the employees were transferred. 
					<b>Outstaffing means employment into your staff of already existing and working Sirin Software personnel.</b>
