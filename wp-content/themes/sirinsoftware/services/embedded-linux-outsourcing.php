			<div class="head">
				<h2>Challenges of&nbsp;the embedded software</h2>				
			</div>
			<div class="descript">
				<p>
					Embedded-software systems pose extraordinary challenges to the software engineer due to their complexity. 
					<b>The main source of complexity is the large number of subtle and often unexpected interactions among 
					the various parts of these systems,</b> which have the following common features:  
				</p>
				<ul>
					<li>functionality represented by states and events;</li>
					<li>real-time behavior of events and expected actions;</li>
					<li>combined software/hardware systems equipped with distributed software, computers, sensors and actuators;</li>
					<li>high demands on availability, safety, information security, and interoperability;</li>
					<li>and long-lived systems in which embedded software is expected to work reliably.</li>
				</ul>
				<p>
					Embedded software development practices vary to a high degree across industries. Because of the high-reliability and quality requirements for embedded applications, as well as stringent performance requirements, the embedded world is necessarily focused on practices that yield high quality software. Compared to other forms of software such as desktop applications, and business information systems, the embedded-software domain tends to use more sophisticated software quality assurance, better quality measurements, formal inspections, and more test stages. 
				</p>
			</div>
