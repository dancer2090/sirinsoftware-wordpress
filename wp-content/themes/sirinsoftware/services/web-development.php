						<img src="<?php echo get_template_directory_uri();?>/img/img-03-03.png" alt="">
					</div>
					<p>
						<span class="title"><b>Data Services</b></span>
						The final tier is the <b>Data Service</b> layer. <b>Data Services store, retrieve, and update information at a high level.</b> Databases, file systems, and writeable media are all examples of data storage and retrieval devices. For Web applications, however, databases are the most practical. Databases allow developers to store, retrieve, add to, and update categorical information in a systematic and organized fashion.
