				<div class="six-steps-plan">
					<ul>
						<li class="s-01">
							<span class="title">Step 1</span>
							<span class="text">
								<b>The interview</b> – the interview is to determine the purpose of your project, the target audience, branding/perception goals, content source, initial technical specs, and communication strategy.
							</span>
						</li>
						<li class="s-02">
							<span class="title">Step 2</span>
							<span class="text">
								<b>The Project Brief</b> – Based on the information gathered from the interview we write the project brief to define in black and white the project goals, audience profile, audience perception, primary message, and competitive advantage.
							</span>
						</li>
						<li class="s-03">
							<span class="title">Step 3</span>
							<span class="text">
								<b>Develop Persona(s)</b> – Looking back at the interview and project brief, we develop a persona for the primary audience. We use the persona template to bring an imaginary user to life by filling in the following details: name, quote, description, demographics, technical profile, top three user goals, and top three business objectives for site.
							</span>
						</li>
						<li class="s-04">
							<span class="title">Step 4</span>
							<span class="text">
								<b>Technical Specifications</b> - We are write the Technical Specifications using the information gathered in the client interview combine with the technical standards of your organization. We fully document the specifications to clearly establish requirements like screen resolution, browser compatibility, download time, web standards, and accessibility.
							</span>
						</li>
						<li class="s-05">
							<span class="title">Step 5</span>
							<span class="text">
								<b>Project Plan/Timeline</b> - Establishing the timeline for deliverables and tasks in regards to each phase of the project helps us assign due dates and resources for the lifecycle of the project.
							</span>
						</li>
						<li class="s-06">
							<span class="title">Step 6</span>
							<span class="text">
								<b>Document Maintenance Considerations</b> – Developing a website maintenance plan that documents how the site will be updated and reviewed regularly is important. Why in the world would you consider maintenance at the beginning of a project? It is important to set clear expectations that websites and or web applications require support and maintenance.
							</span>
						</li>
					</ul>
