			<div class="descript">
				<p>
					<b>The Native approach is the most common way of writing an application.</b> This is a platform specific approach that requires a unique expertise developed with a native SDK. It requires quite a significant workload, because each platform requires writing code in a specific language. In the case of iOS app development, you have to know Objective-C. <b>Thus, producing the code for both an Android App and an iOS App literally doubles the amount of work to be done.</b>
				</p>
				<div class="pic-text">
					<div class="pic">
						<img src="<?php echo get_template_directory_uri();?>/img/img-02-08.png" alt="">
