				</p>
				<p>
					<b>The one big disadvantage mimicking a native app with a web app is that you won’t be able to run it offline.</b> The access to different kinds of hardware is also limited; basically different sensors on your phone that can make your app more interesting and functional. <b>Webs apps meant to mimic native apps cannot be downloaded and installed from the App Store.</b>
